function deepFreeze(constants) {
	var propNames = Object.getOwnPropertyNames(constants);
	propNames.forEach((name) => {
		var prop = constants[name];

		if (typeof prop === 'object' && prop !== null) {
			deepFreeze(prop);
		}
	});

	return Object.freeze(constants);
}

